package org.modelrefactoring.evolution.coed.resource.coed.mopp;

import java.util.Collections;
import java.util.Set;
import org.eclipse.emf.ecore.EStructuralFeature;

/**
 * A representation for a range in a document where a structural feature (e.g., a
 * reference) is expected.
 */
public class CoedExpectedStructuralFeature extends org.modelrefactoring.evolution.coed.resource.coed.mopp.CoedAbstractExpectedElement {
	
	private org.modelrefactoring.evolution.coed.resource.coed.grammar.CoedPlaceholder placeholder;
	
	public CoedExpectedStructuralFeature(org.modelrefactoring.evolution.coed.resource.coed.grammar.CoedPlaceholder placeholder) {
		super(placeholder.getMetaclass());
		this.placeholder = placeholder;
	}
	
	public EStructuralFeature getFeature() {
		return placeholder.getFeature();
	}
	
	/**
	 * Returns the expected placeholder.
	 */
	public org.modelrefactoring.evolution.coed.resource.coed.grammar.CoedSyntaxElement getSymtaxElement() {
		return placeholder;
	}
	
	public String getTokenName() {
		return placeholder.getTokenName();
	}
	
	public Set<String> getTokenNames() {
		return Collections.singleton(getTokenName());
	}
	
	public String toString() {
		return "EFeature " + getFeature().getEContainingClass().getName() + "." + getFeature().getName();
	}
	
	public boolean equals(Object o) {
		if (o instanceof CoedExpectedStructuralFeature) {
			return getFeature().equals(((CoedExpectedStructuralFeature) o).getFeature());
		}
		return false;
	}
	@Override
	public int hashCode() {
		return getFeature().hashCode();
	}
	
}
