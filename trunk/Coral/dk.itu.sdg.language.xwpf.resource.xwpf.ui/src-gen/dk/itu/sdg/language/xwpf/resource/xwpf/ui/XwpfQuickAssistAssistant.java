package dk.itu.sdg.language.xwpf.resource.xwpf.ui;

public class XwpfQuickAssistAssistant extends org.eclipse.jface.text.quickassist.QuickAssistAssistant implements org.eclipse.jface.text.quickassist.IQuickAssistAssistant {
	
	public XwpfQuickAssistAssistant(dk.itu.sdg.language.xwpf.resource.xwpf.IXwpfResourceProvider resourceProvider, dk.itu.sdg.language.xwpf.resource.xwpf.ui.IXwpfAnnotationModelProvider annotationModelProvider) {
		setQuickAssistProcessor(new dk.itu.sdg.language.xwpf.resource.xwpf.ui.XwpfQuickAssistProcessor(resourceProvider, annotationModelProvider));
		setInformationControlCreator(new org.eclipse.jface.text.AbstractReusableInformationControlCreator() {
			public org.eclipse.jface.text.IInformationControl doCreateInformationControl(org.eclipse.swt.widgets.Shell parent) {
				return new org.eclipse.jface.text.DefaultInformationControl(parent, (org.eclipse.jface.text.DefaultInformationControl.IInformationPresenter) null);
			}
		});
	}
	
	public boolean canAssist(org.eclipse.jface.text.quickassist.IQuickAssistInvocationContext invocationContext) {
		return false;
	}
	
	public boolean canFix(org.eclipse.jface.text.source.Annotation annotation) {
		return true;
	}
	
}
