package org.emftext.refactoring.tests.properties.resource.properties.ui;

public class PropertiesOutlinePageExpandAllAction extends org.emftext.refactoring.tests.properties.resource.properties.ui.AbstractPropertiesOutlinePageAction {
	
	public PropertiesOutlinePageExpandAllAction(org.emftext.refactoring.tests.properties.resource.properties.ui.PropertiesOutlinePageTreeViewer treeViewer) {
		super(treeViewer, "Expand all", org.eclipse.jface.action.IAction.AS_PUSH_BUTTON);
		initialize("icons/expand_all_icon.gif");
	}
	
	public void runInternal(boolean on) {
		if (on) {
			getTreeViewer().expandAll();
		}
	}
	
	public boolean keepState() {
		return false;
	}
	
}
